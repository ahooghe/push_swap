#include "../include/libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	totallen;
	size_t	s1_len;
	size_t	s2_len;
	char	*str;

	if (!s1 && !s2)
		return (ft_strdup(""));
	if (s1 && !s2)
		return (ft_strdup(s1));
	if (!s1 && s2)
		return (ft_strdup(s2));
	s1_len = ft_strlen((char *)s1);
	s2_len = ft_strlen(s2);
	totallen = s1_len + s2_len + 1;
	str = malloc(sizeof(char) * totallen);
	if (!str)
		return (0);
	ft_memmove(str, s1, s1_len);
	ft_memmove(str + s1_len, s2, s2_len);
	str[totallen - 1] = '\0';
	return (str);
}
