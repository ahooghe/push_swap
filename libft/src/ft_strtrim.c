#include "../include/libft.h"

int	isinset(char c, const char *set)
{
	while (*set)
		if (c == *set++)
			return (0);
	return (1);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	char	*str;

	if (!s1)
		return (NULL);
	if (!set)
		return (ft_strdup(s1));
	start = 0;
	end = ft_strlen(s1);
	while (isinset(s1[start], set) == 0)
		start++;
	if (start == ft_strlen(s1))
	{
		str = ft_strdup("");
		if (!(str))
			return (NULL);
		else
			return (str);
	}
	while (isinset(s1[end - 1], set) == 0)
		end--;
	str = ft_substr(s1, start, end - start);
	return (str);
}
